static const char* VS_SRC = R"GLSL(#version 300 es
precision highp float;
layout(location=0) in vec3 aPos;
layout(location=1) in vec3 aColor;

uniform mat4 uModel;
uniform mat4 uViewProj;
uniform vec3 uSunDir;

out vec3 vColor;
out vec3 vWorldPos;
out vec3 vNormal;

void main() {
    vec4 wp      = uModel * vec4(aPos, 1.0);
    vWorldPos    = wp.xyz;
    vColor       = aColor;
    // Cheap normal: rotate world-up by model rotation
    mat3 normalMat = mat3(uModel);
    vNormal      = normalize(normalMat * vec3(0.0, 1.0, 0.0));
    gl_Position  = uViewProj * wp;
}
)GLSL";

static const char* FS_SRC = R"GLSL(#version 300 es
precision highp float;

in vec3 vColor;
in vec3 vWorldPos;
in vec3 vNormal;

uniform vec3  uSunDir;
uniform vec3  uSkyColor;
uniform vec3  uCamPos;
uniform float uFogStart;
uniform float uFogEnd;

out vec4 FragColor;

void main() {
    // Hemisphere ambient
    vec3 skyAmb    = vec3(0.42, 0.52, 0.68);
    vec3 groundAmb = vec3(0.14, 0.12, 0.09);
    float hemi     = vNormal.y * 0.5 + 0.5;
    vec3 ambient   = mix(groundAmb, skyAmb, hemi) * 0.38;

    // Lambertian diffuse
    float NdotL  = max(dot(vNormal, uSunDir), 0.0);
    vec3  diffuse = vColor * NdotL * 0.78;

    // Blinn-Phong specular (subtle gloss)
    vec3  viewDir  = normalize(uCamPos - vWorldPos);
    vec3  halfVec  = normalize(uSunDir + viewDir);
    float specPow  = pow(max(dot(vNormal, halfVec), 0.0), 56.0);
    vec3  specular = vec3(0.10) * specPow;

    vec3 lit = ambient + diffuse + specular;

    // Quadratic exponential fog
    float dist      = length(uCamPos - vWorldPos);
    float fog       = clamp((dist - uFogStart) / (uFogEnd - uFogStart), 0.0, 1.0);
    fog             = fog * fog;

    vec3 color = mix(lit, uSkyColor, fog);
    FragColor  = vec4(color, 1.0);
}
)GLSL";

