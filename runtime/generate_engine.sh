// ─────────────────────────────────────────────────────────────
//  Math
// ─────────────────────────────────────────────────────────────
struct Vec3 { float x, y, z; };
struct Mat4 { float m[16]; };

static Mat4 mat4_identity() {
    Mat4 r; memset(r.m, 0, sizeof(r.m));
    r.m[0] = r.m[5] = r.m[10] = r.m[15] = 1.0f;
    return r;
}
static Mat4 mat4_mul(const Mat4& a, const Mat4& b) {
    Mat4 r; memset(r.m, 0, sizeof(r.m));
    for (int i = 0; i < 4; i++)
        for (int j = 0; j < 4; j++)
            for (int k = 0; k < 4; k++)
                r.m[i*4+j] += a.m[k*4+j] * b.m[i*4+k];
    return r;
}
static Mat4 mat4_perspective(float fovY, float aspect, float zn, float zf) {
    Mat4 r = mat4_identity();
    float f = 1.0f / tanf(fovY * 0.5f);
    r.m[0]  =  f / aspect;
    r.m[5]  =  f;
    r.m[10] = -(zf + zn) / (zf - zn);
    r.m[11] = -1.0f;
    r.m[14] = -(2.0f * zf * zn) / (zf - zn);
    r.m[15] =  0.0f;
    return r;
}
static Mat4 mat4_lookat(Vec3 eye, Vec3 center, Vec3 up) {
    auto norm = [](Vec3 v) -> Vec3 {
        float l = sqrtf(v.x*v.x + v.y*v.y + v.z*v.z);
        return {v.x/l, v.y/l, v.z/l};
    };
    auto cross = [](Vec3 a, Vec3 b) -> Vec3 {
        return {a.y*b.z - a.z*b.y, a.z*b.x - a.x*b.z, a.x*b.y - a.y*b.x};
    };
    auto dot = [](Vec3 a, Vec3 b) -> float { return a.x*b.x + a.y*b.y + a.z*b.z; };

    Vec3 f = norm({center.x-eye.x, center.y-eye.y, center.z-eye.z});
    Vec3 s = norm(cross(f, up));
    Vec3 u = cross(s, f);

    Mat4 r = mat4_identity();
    r.m[0]=s.x; r.m[4]=s.y; r.m[8] =s.z;
    r.m[1]=u.x; r.m[5]=u.y; r.m[9] =u.z;
    r.m[2]=-f.x;r.m[6]=-f.y;r.m[10]=-f.z;
    r.m[12]=-dot(s,eye); r.m[13]=-dot(u,eye); r.m[14]=dot(f,eye);
    return r;
}
static Mat4 mat4_translate(float x, float y, float z) {
    Mat4 r = mat4_identity();
    r.m[12] = x; r.m[13] = y; r.m[14] = z;
    return r;
}
static Mat4 mat4_scale(float x, float y, float z) {
    Mat4 r = mat4_identity();
    r.m[0] = x; r.m[5] = y; r.m[10] = z;
    return r;
}
static Mat4 mat4_rotX(float a) {
    Mat4 r = mat4_identity();
    r.m[5]=cosf(a); r.m[6]=sinf(a); r.m[9]=-sinf(a); r.m[10]=cosf(a);
    return r;
}
static Mat4 mat4_rotY(float a) {
    Mat4 r = mat4_identity();
    r.m[0]=cosf(a); r.m[2]=-sinf(a); r.m[8]=sinf(a); r.m[10]=cosf(a);
    return r;
}

// ─────────────────────────────────────────────────────────────
//  Procedural terrain height  (5-octave fBm)
// ─────────────────────────────────────────────────────────────
static float hash2(float x, float y) {
    float v = sinf(x * 127.1f + y * 311.7f) * 43758.5453f;
    return v - floorf(v);
}
static float vnoise(float x, float y) {
    float ix = floorf(x), iy = floorf(y);
    float fx = x - ix,    fy = y - iy;
    float ux = fx*fx*(3.0f - 2.0f*fx);
    float uy = fy*fy*(3.0f - 2.0f*fy);
    float a = hash2(ix,   iy);
    float b = hash2(ix+1, iy);
    float c = hash2(ix,   iy+1);
    float d = hash2(ix+1, iy+1);
    return a + (b-a)*ux + (c-a)*uy + (d-b+a-c)*ux*uy;
}
static float terrainH(float wx, float wz) {
    float v = 0.0f, a = 0.55f, s = 0.18f;
    for (int i = 0; i < 5; i++) {
        v += a * vnoise(wx * s, wz * s);
        s *= 2.1f; a *= 0.50f;
    }
    return v * 9.5f - 0.6f;   // -0.6 puts flat areas near y=0
}

