// ─────────────────────────────────────────────────────────────
//  Character assembly
// ─────────────────────────────────────────────────────────────
static void drawCharacter(const Mat4& base) {

    // --- Torso ---
    setModel(base);
    drawVAO(g_vaoTorso, N_TORSO);

    // --- Head ---
    Mat4 mHead = mat4_mul(base, mat4_translate(0.0f, 0.90f, 0.0f));
    setModel(mHead);
    drawVAO(g_vaoHead, N_HEAD);

    // --- Arms ---
    // Right arm (sword side): swings during attack
    float swRot = (g_slashT > 0.0f)
                  ? -2.4f * sinf(g_slashT * 3.14159f)
                  : -sinf(g_walkT) * 0.45f;
    Mat4 mRArm = mat4_mul(base, mat4_translate( 0.40f, 0.55f, 0.0f));
    mRArm = mat4_mul(mRArm, mat4_rotX(swRot));
    setModel(mRArm);                           drawVAO(g_vaoUpLimb, N_UP_LIMB);
    Mat4 mRFore = mat4_mul(mRArm, mat4_translate(0.0f, -0.44f, 0.0f));
    setModel(mRFore);                          drawVAO(g_vaoLowLimb, N_LOW_LIMB);
    Mat4 mRHand = mat4_mul(mRFore, mat4_translate(0.0f, -0.42f, 0.0f));
    setModel(mRHand);                          drawVAO(g_vaoFoot, N_FOOT);
    // Sword in right hand
    Mat4 mSword = mat4_mul(mRHand, mat4_translate(0.0f, -0.25f, 0.0f));
    setModel(mSword);                          drawVAO(g_vaoSword, N_SWORD);

    // Left arm (shield side): raised when blocking
    float shRot = g_block ? -1.5f : (g_bashT > 0.f ? -1.75f : sinf(g_walkT) * 0.45f);
    Mat4 mLArm = mat4_mul(base, mat4_translate(-0.40f, 0.55f, 0.0f));
    mLArm = mat4_mul(mLArm, mat4_rotX(shRot));
    setModel(mLArm);                           drawVAO(g_vaoUpLimb, N_UP_LIMB);
    Mat4 mLFore = mat4_mul(mLArm, mat4_translate(0.0f, -0.44f, 0.0f));
    setModel(mLFore);                          drawVAO(g_vaoLowLimb, N_LOW_LIMB);
    Mat4 mLHand = mat4_mul(mLFore, mat4_translate(0.0f, -0.42f, 0.0f));
    // Shield on left forearm, rotated upright
    Mat4 mShield = mat4_mul(mLHand, mat4_translate(0.0f, -0.15f, 0.08f));
    mShield = mat4_mul(mShield, mat4_rotX(1.5708f));
    setModel(mShield);                         drawVAO(g_vaoShield, N_SHIELD);

    // --- Legs ---
    float legSwing = sinf(g_walkT) * 0.65f;
    // Right leg
    Mat4 mRLeg  = mat4_mul(base, mat4_translate( 0.20f, -0.08f, 0.0f));
    mRLeg = mat4_mul(mRLeg, mat4_rotX(-legSwing));
    setModel(mRLeg);                           drawVAO(g_vaoUpLimb, N_UP_LIMB);
    Mat4 mRShin = mat4_mul(mRLeg, mat4_translate(0.0f, -0.44f, 0.0f));
    // Knee bend: forward leg bends back
    float rKnee = std::max(0.0f, -legSwing) * 0.6f;
    mRShin = mat4_mul(mRShin, mat4_rotX(rKnee));
    setModel(mRShin);                          drawVAO(g_vaoLowLimb, N_LOW_LIMB);
    Mat4 mRFoot = mat4_mul(mRShin, mat4_translate(0.0f, -0.44f, 0.0f));
    setModel(mRFoot);                          drawVAO(g_vaoFoot, N_FOOT);

    // Left leg
    Mat4 mLLeg  = mat4_mul(base, mat4_translate(-0.20f, -0.08f, 0.0f));
    mLLeg = mat4_mul(mLLeg, mat4_rotX( legSwing));
    setModel(mLLeg);                           drawVAO(g_vaoUpLimb, N_UP_LIMB);
    Mat4 mLShin = mat4_mul(mLLeg, mat4_translate(0.0f, -0.44f, 0.0f));
    float lKnee = std::max(0.0f,  legSwing) * 0.6f;
    mLShin = mat4_mul(mLShin, mat4_rotX(lKnee));
    setModel(mLShin);                          drawVAO(g_vaoLowLimb, N_LOW_LIMB);
    Mat4 mLFoot = mat4_mul(mLShin, mat4_translate(0.0f, -0.44f, 0.0f));
    setModel(mLFoot);                          drawVAO(g_vaoFoot, N_FOOT);
}

