static GLuint compileShader(GLenum type, const char* src) {
    GLuint s = glCreateShader(type);
    glShaderSource(s, 1, &src, nullptr);
    glCompileShader(s);
    GLint ok; glGetShaderiv(s, GL_COMPILE_STATUS, &ok);
    if (!ok) {
        char buf[512]; glGetShaderInfoLog(s, 512, nullptr, buf);
        LOGE("Shader error: %s", buf);
    }
    return s;
}

static GLuint createProgram(const char* vs, const char* fs) {
    GLuint p = glCreateProgram();
    glAttachShader(p, compileShader(GL_VERTEX_SHADER, vs));
    glAttachShader(p, compileShader(GL_FRAGMENT_SHADER, fs));
    glLinkProgram(p);
    GLint ok; glGetProgramiv(p, GL_LINK_STATUS, &ok);
    if (!ok) { char b[512]; glGetProgramInfoLog(p, 512, nullptr, b); LOGE("Link: %s", b); }
    return p;
}

// VAO for position+colour data (6 floats per vertex, stride=24)
static GLuint makeVAO(const float* data, int vertCount) {
    GLuint vao, vbo;
    glGenVertexArrays(1, &vao);
    glGenBuffers(1, &vbo);
    glBindVertexArray(vao);
    glBindBuffer(GL_ARRAY_BUFFER, vbo);
    glBufferData(GL_ARRAY_BUFFER, (GLsizeiptr)(vertCount * 6 * sizeof(float)),
                 data, GL_STATIC_DRAW);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 24, (void*)0);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 24, (void*)12);
    glEnableVertexAttribArray(1);
    glBindVertexArray(0);
    return vao;
}

// ─────────────────────────────────────────────────────────────
//  Runtime terrain mesh generation
// ─────────────────────────────────────────────────────────────
static const int  TERRAIN_CELLS = 64;    // cells per side
static const float TERRAIN_SCALE = 1.0f; // world units per cell
static GLuint  g_vaoTerrain = 0;
static GLuint  g_vboTerrain = 0;
static GLuint  g_iboTerrain = 0;
static int     g_terrainIdxCount = 0;

static void buildTerrain() {
    int N = TERRAIN_CELLS + 1;
    std::vector<float>    verts;
    std::vector<uint32_t> idx;
    verts.reserve(N * N * 6);
    idx  .reserve(TERRAIN_CELLS * TERRAIN_CELLS * 6);

    float half = TERRAIN_CELLS * TERRAIN_SCALE * 0.5f;

    for (int z = 0; z < N; z++) {
        for (int x = 0; x < N; x++) {
            float wx = x * TERRAIN_SCALE - half;
            float wz = z * TERRAIN_SCALE - half;
            float wy = terrainH(wx, wz);

            // Normal via finite differences
            float hL = terrainH(wx - 0.1f, wz);
            float hR = terrainH(wx + 0.1f, wz);
            float hD = terrainH(wx, wz - 0.1f);
            float hU = terrainH(wx, wz + 0.1f);
            float nx = hL - hR, nz = hD - hU, ny = 0.2f;
            float nl = sqrtf(nx*nx + ny*ny + nz*nz);
            nx /= nl; ny /= nl; nz /= nl;

            // Height-based colour: grass → dirt → rock
            float t = wy / 5.0f;
            float r, g, b;
            if (t < 0.2f) {
                // Low: wet soil / mud
                r = 0.28f; g = 0.20f; b = 0.10f;
            } else if (t < 0.65f) {
                // Mid: lush grass
                float ft = (t - 0.2f) / 0.45f;
                r = 0.16f + ft * 0.08f;
                g = 0.40f + ft * 0.06f;
                b = 0.12f + ft * 0.04f;
            } else {
                // High: rocky outcrop
                float ft = std::min((t - 0.65f) / 0.35f, 1.0f);
                r = 0.24f + ft * 0.28f;
                g = 0.20f + ft * 0.24f;
                b = 0.16f + ft * 0.20f;
            }

            // AO darkening at depression troughs (very subtle)
            float ao = 0.82f + 0.18f * std::max(ny, 0.0f);
            verts.push_back(wx); verts.push_back(wy); verts.push_back(wz);
            verts.push_back(r * ao); verts.push_back(g * ao); verts.push_back(b * ao);
        }
    }
    for (int z = 0; z < TERRAIN_CELLS; z++) {
        for (int x = 0; x < TERRAIN_CELLS; x++) {
            uint32_t s = z * N + x;
            idx.push_back(s);     idx.push_back(s + N); idx.push_back(s + 1);
            idx.push_back(s + 1); idx.push_back(s + N); idx.push_back(s + N + 1);
        }
    }
    g_terrainIdxCount = (int)idx.size();

    glGenVertexArrays(1, &g_vaoTerrain);
    glGenBuffers(1, &g_vboTerrain);
    glGenBuffers(1, &g_iboTerrain);
    glBindVertexArray(g_vaoTerrain);
    glBindBuffer(GL_ARRAY_BUFFER, g_vboTerrain);
    glBufferData(GL_ARRAY_BUFFER, verts.size() * sizeof(float), verts.data(), GL_STATIC_DRAW);
    glBindBuffer(GL_ELEMENT_ARRAY_BUFFER, g_iboTerrain);
    glBufferData(GL_ELEMENT_ARRAY_BUFFER, idx.size() * sizeof(uint32_t), idx.data(), GL_STATIC_DRAW);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 24, (void*)0);  glEnableVertexAttribArray(0);
    glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 24, (void*)12); glEnableVertexAttribArray(1);
    glBindVertexArray(0);
}

