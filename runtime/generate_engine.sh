JNIEXPORT void JNICALL
Java_com_game_procedural_MainActivity_onCreated(JNIEnv*, jobject) {
    g_prog = createProgram(VS_SRC, FS_SRC);

    // Cache uniforms
    uModel     = glGetUniformLocation(g_prog, "uModel");
    uViewProj  = glGetUniformLocation(g_prog, "uViewProj");
    uSunDir    = glGetUniformLocation(g_prog, "uSunDir");
    uSkyColor  = glGetUniformLocation(g_prog, "uSkyColor");
    uCamPos    = glGetUniformLocation(g_prog, "uCamPos");
    uFogStart  = glGetUniformLocation(g_prog, "uFogStart");
    uFogEnd    = glGetUniformLocation(g_prog, "uFogEnd");

    // Upload model geometry
    g_vaoTorso   = makeVAO(M_TORSO,   N_TORSO);
    g_vaoHead    = makeVAO(M_HEAD,    N_HEAD);
    g_vaoUpLimb  = makeVAO(M_UP_LIMB, N_UP_LIMB);
    g_vaoLowLimb = makeVAO(M_LOW_LIMB,N_LOW_LIMB);
    g_vaoFoot    = makeVAO(M_FOOT,    N_FOOT);
    g_vaoSword   = makeVAO(M_SWORD,   N_SWORD);
    g_vaoShield  = makeVAO(M_SHIELD,  N_SHIELD);
    g_vaoTree    = makeVAO(M_TREE,    N_TREE);

    buildTerrain();

    glEnable(GL_DEPTH_TEST);
    glDepthFunc(GL_LESS);
    glEnable(GL_CULL_FACE);
    glCullFace(GL_BACK);
    glClearColor(SKY_R, SKY_G, SKY_B, 1.0f);

    LOGI("Engine initialised.");
}

JNIEXPORT void JNICALL
Java_com_game_procedural_MainActivity_onChanged(JNIEnv*, jobject, jint w, jint h) {
    glViewport(0, 0, w, h);
    g_aspect = (h > 0) ? (float)w / (float)h : 1.0f;
    g_proj   = mat4_perspective(1.047f, g_aspect, 0.1f, 160.0f);
    LOGI("Surface %dx%d", w, h);
}

JNIEXPORT void JNICALL
Java_com_game_procedural_MainActivity_onDraw(
        JNIEnv*, jobject,
        jfloat ix, jfloat iy,
        jfloat yaw, jfloat pitch, jfloat zoom)
{
    // Store camera params
    g_camYaw   = yaw;
    g_camPitch = pitch;
    g_camZoom  = zoom;

    // ── Physics / Movement ─────────────────────────────────
    const float SPEED = 0.12f;
    bool moving = (fabsf(ix) > 0.02f || fabsf(iy) > 0.02f);
    if (moving) {
        float sinY = sinf(yaw), cosY = cosf(yaw);
        float dx =  ix * cosY - (-iy) * sinY;
        float dz =  ix * sinY + (-iy) * cosY;
        g_px    += dx * SPEED;
        g_pz    -= dz * SPEED;
        g_facing = atan2f(-dx, dz);
        g_walkT += 0.18f;
    }

    // Jump physics
    g_jumpY  += g_jumpVY;
    g_jumpVY -= 0.022f;
    float gh  = terrainH(g_px, g_pz);
    if (g_jumpY <= gh) { g_jumpY = gh; g_jumpVY = 0.f; }
    g_py = g_jumpY;

    // Animation decay
    if (g_slashT > 0.f) g_slashT -= 0.055f;
    if (g_bashT  > 0.f) g_bashT  -= 0.085f;

    // ── Camera ─────────────────────────────────────────────
    float safeZoom = std::max(5.0f, zoom);
    float eyeX = g_px - sinf(yaw) * cosf(pitch) * safeZoom;
    float eyeZ = g_pz - cosf(yaw) * cosf(pitch) * safeZoom;
    float eyeY = g_py + sinf(pitch) * safeZoom + 1.4f;
    Vec3  eye    = {eyeX, eyeY, eyeZ};
    Vec3  center = {g_px, g_py + 1.2f, g_pz};
    Mat4  view   = mat4_lookat(eye, center, {0.f, 1.f, 0.f});
    Mat4  vp     = mat4_mul(g_proj, view);

    // ── Render ─────────────────────────────────────────────
    glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
    glUseProgram(g_prog);

    glUniformMatrix4fv(uViewProj, 1, GL_FALSE, vp.m);
    glUniform3f(uSunDir,   SUN_X, SUN_Y, SUN_Z);
    glUniform3f(uSkyColor, SKY_R, SKY_G, SKY_B);
    glUniform3f(uCamPos,   eyeX, eyeY, eyeZ);
    glUniform1f(uFogStart, safeZoom * 3.5f);
    glUniform1f(uFogEnd,   safeZoom * 7.5f);

    // 1. Terrain
    Mat4 terrainM = mat4_identity();
    setModel(terrainM);
    glBindVertexArray(g_vaoTerrain);
    glDrawElements(GL_TRIANGLES, g_terrainIdxCount, GL_UNSIGNED_INT, nullptr);

    // 2. Trees — procedurally placed on terrain
    for (int i = -4; i <= 4; i++) {
        for (int j = -4; j <= 4; j++) {
            float tx = floorf(g_px / 10.0f) * 10.0f + i * 10.0f;
            float tz = floorf(g_pz / 10.0f) * 10.0f + j * 10.0f;
            // Deterministic filter so not every cell has a tree
            float h1 = hash2(tx * 0.05f, tz * 0.07f);
            if (h1 < 0.45f) continue;
            float ty = terrainH(tx, tz);
            // Skip if terrain is too steep / rocky (high altitude)
            if (ty > 6.0f) continue;
            Mat4 tm = mat4_translate(tx, ty, tz);
            // Slight random rotation per tree
            float rot = hash2(tx * 0.13f, tz * 0.19f) * 6.28f;
            tm = mat4_mul(tm, mat4_rotY(rot));
            setModel(tm);
            drawVAO(g_vaoTree, N_TREE);
        }
    }

    // 3. Player character
    float bobY   = (moving ? sinf(g_walkT * 2.0f) * 0.03f : 0.0f);
    float jumpArc = g_jumpY - terrainH(g_px, g_pz);   // height above ground
    Mat4 playerBase = mat4_translate(g_px, g_py + 1.0f + jumpArc + bobY, g_pz);
    playerBase = mat4_mul(playerBase, mat4_rotY(g_facing));
    drawCharacter(playerBase);
}

JNIEXPORT void JNICALL
Java_com_game_procedural_MainActivity_triggerAction(JNIEnv*, jobject, jint id) {
    switch (id) {
        case 1: g_slashT = 1.0f; break;                           // Attack
        case 2: g_block  = true; break;                           // Block start
        case 3: g_block  = false; break;                          // Block end
        case 4:                                                    // Jump
            if (g_jumpY <= terrainH(g_px, g_pz) + 0.05f)
                g_jumpVY = 0.38f;
            break;
        case 6: g_bashT = 1.0f; g_block = false; break;          // Shield bash
    }
}

JNIEXPORT jfloat JNICALL
Java_com_game_procedural_MainActivity_getCameraYaw(JNIEnv*, jobject) {
    return g_camYaw;
}

